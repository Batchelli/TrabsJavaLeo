public class HexagonoRetangular {
    double baseHexagono;
    double alturaHexagono;
    HexagonoRetangular(){
        this.baseHexagono = baseHexagono;
        this.alturaHexagono = alturaHexagono;
    }
    double areaHexagono(){return (baseHexagono * alturaHexagono) / 2 * 6;}
    double perimetroHexagono(){return baseHexagono * 6;}
}

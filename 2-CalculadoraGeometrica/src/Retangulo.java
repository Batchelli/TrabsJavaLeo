public class Retangulo {
    double baseRetangulo;
    double alturaRetangulo;
    Retangulo(){
        this.baseRetangulo = baseRetangulo;
        this.alturaRetangulo = alturaRetangulo;
    }
    double areaRetangulo(){return baseRetangulo * alturaRetangulo;}
    double perimetroRetangulo(){return (2 * baseRetangulo) + (2 * alturaRetangulo);}
}

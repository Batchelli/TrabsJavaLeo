// Item.java
public class Item {
    private String nome;
    private double preco;

    // Construtor
    public Item(String nome, double preco) {
        this.nome = nome;
        this.preco = preco;
    }

    // Getters
    public String getNome() {
        return nome;
    }

    public double getPreco() {
        return preco;
    }

}
